public class EvenOddNumber {
    public static void main(String[] args) {
        int num = 9; // primitive data type been created
        switch (num % 2){ // using switch method to check odd and even number
            case 0:
                System.out.println("even number"); // when the number divide by 2 it should show even number
                break;
            case 1:
                System.out.println("odd number");// when the number has one reminder then it should show odd number


        }



    }



}
